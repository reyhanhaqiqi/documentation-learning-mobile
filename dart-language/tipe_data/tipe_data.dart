/* Belajar Menggunakan Tipe Data */
void main() {
  /* Declare the variables with tipe data */
  /* Tipe Data String */
  var name = 'Reyhan Haqiqi';
  /* Tipe Data Integer/Number */
  var number = 20;
  /* Tipe Data Float/Double */
  var float = 3.32;
  /* Tipe Data Boolean */
  var booleanTrue = true;
  var booleanFalse = false;
  /* Tipe Data Array/List */
  var list = ['Saya', 'Berumur', 20, 'Tahun', true];
  /* Tipe Data Object */
  var object = {
    'month': ['april'],
    'link': '//path/to/april.jpg',
  };

  /* Call the variables */
  print(name);
  print('Hallo nama saya ${name}');
  print('');
  print(number);
  print('');
  print(float);
  print('');
  print(booleanTrue);
  print(booleanFalse);
  print('');
  print(list);
  print(list);
  print('');
  print(object);
  print('');
  print('Halo nama ${list[0]} $name ${list[1]} ${list[2]} ${list[3]}');
  print('');
}
