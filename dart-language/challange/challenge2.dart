void main() {
  /* Keliling Persegi */
  int s = 10;

  int kelilingPersegi = s * 4;
  print(kelilingPersegi);

  /* Luas Lingkaran */
  int r = 15;

  num luasLingkaran = 22 / 7 * r * r;
  print(luasLingkaran);

  /* Volume Balok */
  int p = 10;
  int l = 20;
  int t = 15;

  int volumeBalok = p * l * t;
  print(volumeBalok);
}
