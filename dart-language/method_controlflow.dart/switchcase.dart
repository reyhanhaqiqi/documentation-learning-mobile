void main() {
  // Switch Case statement
  switchCase();
}

void switchCase() {
  String abjad = 'D';
  switch (abjad) {
    case 'A':
      print('Sangat Baik');
      break;
    case 'B':
      print('Baik');
      break;
    case 'C':
      print('Cukup');
      break;
    case 'D':
      print('Kurang');
      break;
    default:
  }
}
